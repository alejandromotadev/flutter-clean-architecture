import 'package:clean_architecture_bloc/features/posts/domain/entities/post.dart';

class PostModel extends Post {
  PostModel({
    required int id,
    required String title,
    required String body,
    required int userId
  }) : super(id: id, title: title, body: body, userId: userId);

  factory PostModel.fromJson(Map<String, dynamic> json) {
    return PostModel(
      id: json['id'],
      title: json['title'],
      body: json['body'],
      userId: json['userId']
    );
  }

  factory PostModel.fromEntity(Post post) {
    return PostModel(
      id: post.id,
      title: post.title,
      body: post.body,
      userId: post.userId
    );
  }
}